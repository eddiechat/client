// Flatten all emojis for search
export const allEmojis: Emoji[] = emojiCategories.flatMap((cat) => cat.emojis);

// Search emojis by name or alias
export function searchEmojis(query: string, limit = 8): Emoji[] {
  const lowerQuery = query.toLowerCase();
  const results: Emoji[] = [];
  const seen = new Set<string>();

  // First pass: exact name match
  for (const emoji of allEmojis) {
    if (emoji.name === lowerQuery && !seen.has(emoji.emoji)) {
      results.push(emoji);
      seen.add(emoji.emoji);
    }
  }

  // Second pass: name starts with query
  for (const emoji of allEmojis) {
    if (results.length >= limit) break;
    if (emoji.name.startsWith(lowerQuery) && !seen.has(emoji.emoji)) {
      results.push(emoji);
      seen.add(emoji.emoji);
    }
  }

  // Third pass: alias starts with query
  for (const emoji of allEmojis) {
    if (results.length >= limit) break;
    if (
      emoji.aliases.some((alias) => alias.startsWith(lowerQuery)) &&
      !seen.has(emoji.emoji)
    ) {
      results.push(emoji);
      seen.add(emoji.emoji);
    }
  }

  // Fourth pass: name contains query
  for (const emoji of allEmojis) {
    if (results.length >= limit) break;
    if (emoji.name.includes(lowerQuery) && !seen.has(emoji.emoji)) {
      results.push(emoji);
      seen.add(emoji.emoji);
    }
  }

  // Fifth pass: alias contains query
  for (const emoji of allEmojis) {
    if (results.length >= limit) break;
    if (
      emoji.aliases.some((alias) => alias.includes(lowerQuery)) &&
      !seen.has(emoji.emoji)
    ) {
      results.push(emoji);
      seen.add(emoji.emoji);
    }
  }

  return results;
}
